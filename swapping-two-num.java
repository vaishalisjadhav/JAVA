public class swapping {
		    public static void main(String [] args){
		        int a = 10;
		        int b= 20;
		        System.out.println("Before swapping \n a is " + a + "\n b is " + b);
		        a=a+b;
		        b=a-b;
		        a=a-b;
		        System.out.println("After swapping \n a is " + a + "\n b is " + b);
	}
}